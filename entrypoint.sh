#!/bin/sh

set -e

echo
echo "-- Environment variables ----------------------------------------------"
env
echo "-----------------------------------------------------------------------"

echo
echo "-- Event JSON ---------------------------------------------------------"
cat "$GITHUB_EVENT_PATH" | jq -M .
echo "-----------------------------------------------------------------------"
echo
if [ ! -z "$1" ]; then
  echo "-- File $1 -----------------------------------"
  sh -c "cat $1"
  echo "-----------------------------------------------------------------------"
fi
